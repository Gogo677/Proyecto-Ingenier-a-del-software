    private void botonConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConfirmaActionPerformed
        // TODO add your handling code here:
        if(!correo.getText().equalsIgnoreCase("") ||  !contraseña.getText().equalsIgnoreCase("")){
            
        
        BD load = new BD();
        
        usuario.setNombre(nombre.getText());
        usuario.setApellidoPat(apellidos.getText());
        usuario.setApellidoMat(apellidos1.getText());
        usuario.setCorreo(correo.getText());
        usuario.setContraseña(contraseña.getText());
        try {
            load.conectaActualizaUsuario(usuario);
        } catch (SQLException ex) {
            Logger.getLogger(MenuUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Los datos se actualizaron");
        
        
        MenuUsuario submenu = new MenuUsuario(usuario);
            submenu.setVisible(true);
            this.dispose();
            
        }
    }//GEN-LAST:event_botonConfirmaActionPerformed

