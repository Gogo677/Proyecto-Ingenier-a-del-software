    private void confEliminacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confEliminacionActionPerformed
        BD load = new BD();
        try {
            load.conectaEliminaUsuario(usuario);
        } catch (SQLException ex) {
            Logger.getLogger(MenuUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        JOptionPane.showMessageDialog(null, "Se elimino el usuario");
        Menu ventana = null;
        try {
            ventana = new Menu();
        } catch (SQLException ex) {
            Logger.getLogger(MenuUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        ventana.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_confEliminacionActionPerformed

