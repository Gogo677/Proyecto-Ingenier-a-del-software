    public void generaLista(String filtro,int filtroTipo) throws SQLException{
        listaProd= new ArrayList();
        
        BD load = new BD();
        
        load.conectaListaProductos(listaProd,filtro,filtroTipo);
        
        String[] listado =new String[listaProd.size()];
        
        for(int i=0;i<listaProd.size();i++){
            listado[i]=listaProd.get(i).cadenaProducto();
        }
        productos.setListData(listado);
        
    }
    
    public void actualizaProducto(){
        List<String> lista;
        lista = new ArrayList();
        lista.add("hola");
        lista.add("jaja");
        
        String[] vector= new String[lista.size()];
        for(int i=0; i<lista.size(); i++){
            vector[i]=lista.get(i);
        }
        productos.setListData(vector);
    }
    
    public void ClickUsuario(Usuario us){
        if(us==null){
            InicioSesion ini = new InicioSesion();
            ini.setVisible(true);
            this.dispose();
        }else{
            MenuUsuario submenu = new MenuUsuario(us);
            submenu.setVisible(true);
            this.dispose();
        }
    }
    
