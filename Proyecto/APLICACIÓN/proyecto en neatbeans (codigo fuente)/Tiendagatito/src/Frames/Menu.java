    private void botonAgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarCarritoActionPerformed
        // TODO add your handling code here:
        int lugarItem=productos.getSelectedIndex();
        if(carritoProd.size()>=0 && carritoProd.size()<15 && lugarItem>=0)
        {
            carritoProd.add(listaProd.get(lugarItem));
            System.out.print("carritoProdsize:"+carritoProd.size());
            
        }else{
            JOptionPane.showMessageDialog(null, "Solo se permite hasta 15 productos por pedido\n Disculpe las molestias");
        }
        contadorItems.setText("Total items en el carrito: "+(carritoProd.size()));
        
    }//GEN-LAST:event_botonAgregarCarritoActionPerformed

